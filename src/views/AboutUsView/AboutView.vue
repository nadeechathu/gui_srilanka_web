<script>
export default {
    data() {
        return {
            startDate: '', // Start date string
            startYear: 2011, // Start year
            startMonth: 4, // Start month
            startDay: 22, // Start day
            companyAge: 0 // Initial value for the company age
        };
    },
    mounted() {
        this.$nextTick(() => {
            const currentDate = new Date();
            const currentYear = currentDate.getFullYear();
            const currentMonth = currentDate.getMonth() + 1; // Months are zero-based, so add 1
            const currentDay = currentDate.getDate();

            // Calculate the company age
            this.companyAge = currentYear - this.startYear;

            // Adjust the age if the current date is before the company's anniversary
            if (currentMonth < this.startMonth || (currentMonth === this.startMonth && currentDay < this.startDay)) {
                this.companyAge--;
            }

            // Set the start date string
            this.startDate = `${this.startYear}/${this.startMonth}/${this.startDay}`;
        });
    }
};
