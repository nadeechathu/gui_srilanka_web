<script>
export default {
   name: "IndustriesView",
   components: {},

   watch: {
    '$route'(to) {
      if (to.params.id) {
        this.scrollToElement(to.params.id);
      }
    }
  },
  mounted() {
    if (this.$route.params.id) {
      // Ensure the DOM is ready before scrolling
      this.$nextTick(() => {
        this.scrollToElement(this.$route.params.id);
      });
    }
  },
  methods: {
    scrollToElement(id) {
      const element = document.getElementById(id);
      if (element) {
        element.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    }
  }
};
